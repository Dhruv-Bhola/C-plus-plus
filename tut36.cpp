// Inheritance in CPP (OOPS)
// DRY principle 
// (Donot repeat yourself) in programming: If you repeat to jaha jaha mistake hogi us har jagha change karna hoga

//  TYPES:
// Single Inheritance: One base class One derived class : A ----> (derives) B : A <---- (is derived from) B

// Multiple Inheritance: a derived class from More than one base class 
//        A       B
//       |       |
//           C

// Hierarchical Inheritance: several derived classes from a single base class
//           A
//       |       |
//      B        C

// Multilevel Inheritance: derived class from already derived class : A ----> B ----> C : B = A + More... : C = B + More...

// Hybrid Inheritance: Combination of Multiple & Multilevel inheritance : One of parent classes is not a base class.
//           A
//       |       |
//      B       C
//      |       |
//         D

